function main() {
  const articleHtml = buildArticleHtml();
  const text = stripTags(articleHtml);
  const findings = [];
  if (text.length < 12000) findings.push({ code: "short", actual: text.length });
  if (countRe(articleHtml, /<h2\b/gi) < 7) findings.push({ code: "h2" });
  if (countRe(articleHtml, /<h3\b/gi) < 3) findings.push({ code: "h3" });
  if (countRe(articleHtml, /<p\b/gi) < 24) findings.push({ code: "p" });
  if (countRe(articleHtml, /<details\b/gi) < 5)
    findings.push({
      code: "details",
      actual: countRe(articleHtml, /<details\b/gi),
    });
  if (
    countRe(
      articleHtml,
      /<a\b[^>]+href=["']https?:\/\/wordprais\.ru\//gi,
    ) < 4
  )
    findings.push({ code: "links" });
  if (countRe(articleHtml, /<script\b[^>]+application\/ld\+json/gi) < 1)
    findings.push({ code: "jsonld" });

  mkdirSync(ART, { recursive: true });
  mkdirSync(FIX, { recursive: true });
  writeFileSync(path.join(ART, "article-body-preview.html"), articleHtml, "utf-8");
  writeFileSync(path.join(FIX, "wp-article-latest.html"), articleHtml, "utf-8");
  writeFileSync(
    path.join(ART, "article-gate-smoke.json"),
    JSON.stringify(
      {
        textCharacters: text.length,
        h2: countRe(articleHtml, /<h2\b/gi),
        h3: countRe(articleHtml, /<h3\b/gi),
        p: countRe(articleHtml, /<p\b/gi),
        details: countRe(articleHtml, /<details\b/gi),
        internalLinks: countRe(
          articleHtml,
          /<a\b[^>]+href=["']https?:\/\/wordprais\.ru\//gi,
        ),
        jsonLd: countRe(articleHtml, /<script\b[^>]+application\/ld\+json/gi),
        findings,
      },
      null,
      2,
    ),
    "utf-8",
  );

  if (findings.length) {
    console.error(JSON.stringify({ ok: false, findings }, null, 2));
    process.exit(1);
  }
  console.log(JSON.stringify({ ok: true, textCharacters: text.length }));
}

main();
